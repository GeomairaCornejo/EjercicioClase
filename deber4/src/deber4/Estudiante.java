package deber4;

public class Estudiante extends Persona {
	private int Status;
	private int primerAño;
	private int segundoAño;
	private int tercerAño;
	private int cuartoAño;
	public int getPrimerAño() {
		return primerAño;
	}
	public void setPrimerAño(int primerAño) {
		this.primerAño = primerAño;
	}
	public int getSegundoAño() {
		return segundoAño;
	}
	public void setSegundoAño(int segundoAño) {
		this.segundoAño = segundoAño;
	}
	public int getTercerAño() {
		return tercerAño;
	}
	public void setTercerAño(int tercerAño) {
		this.tercerAño = tercerAño;
	}
	public int getCuartoAño() {
		return cuartoAño;
	}
	public void setCuartoAño(int cuartoAño) {
		this.cuartoAño = cuartoAño;
	}
	
}

